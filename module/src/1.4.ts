// Basic Data Types

// String
let FirstName: string = 'Abubakar';

// number
let age: number = 25;

// boolean
let isStudent: boolean = true;

// null
let isNull: null = null;

// undefined
let isUndefined: undefined = undefined;

// any
let d;
d = 1;
d = 'Abubakar';
d = true;
d = null;
d = undefined;


// Non-primitive data types
// Array

let friends: string[] = ['Ali', 'Ahmed', 'Sara'];
let numbers: number[] = [1, 2, 3, 4, 5];

// tuple
let tuple: [string, number] = ['Abubakar', 25];
